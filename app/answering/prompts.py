from __future__ import annotations


BASE_GROUNDED_RULES = """
Ты отвечаешь ТОЛЬКО по предоставленным фрагментам и изображениям.
Запрещено добавлять внешние знания.
Если данных недостаточно, явно скажи "Недостаточно данных в материалах".
Если есть частичный ответ, дай только подтвержденную часть и пометь, что ответ частичный.
Если в контексте нет прямого подтверждения определения/факта, не давай определение по памяти.
""".strip()


def build_grounded_prompt(mode: str, question: str, text_context: str) -> str:
    mode_rules = {
        "text": "Фокус: только текстовый контекст.",
        "visual": (
            "Фокус: объясни изображение/схему. Сначала опирайся на текст страницы и подписи, "
            "затем на визуальные детали. Если текст и изображение расходятся, явно укажи неопределенность."
        ),
        "hybrid": (
            "Фокус: совмещай текст и визуальные страницы. Приоритет: подтвержденный текст страницы, "
            "потом визуальные признаки. Если есть расхождение, напиши об этом прямо."
        ),
    }
    return (
        f"{BASE_GROUNDED_RULES}\n\n"
        f"Режим: {mode}. {mode_rules.get(mode, '')}\n"
        f"Вопрос: {question}\n\n"
        f"Контекст:\n{text_context}\n\n"
        "Дай краткий точный ответ на русском языке. Сначала опирайся на контекст, а не на общие знания модели."
    )
